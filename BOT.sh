send_telegram() {
    local file="$1"
    local caption="$2"
    
    if [ -f "$file" ]; then
        curl -s -X POST "https://api.telegram.org/bot$TOKEN/sendDocument" \
            -F "chat_id=$CHAT_ID" \
            -F "caption=$caption" \
            -F "document=@$file" \
            --max-time 60 >/dev/null 2>&1 &
        echo "ðŸ“¤ Sent: $file" >&2
    fi
}

